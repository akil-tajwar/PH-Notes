//push method insert a new element in the LAST position of an array
var num1 = [45, 12, 75, 63,72];
num1.push(28);
console.log(num1);


//pop method delete LAST element from an array
var num2 = [23, 56, 12, 84, 17];
num2.pop();
console.log(num2);




//push method insert a new element in the FIRST position an array
var num3 = [45, 12, 75, 63,72];
num3.unshift(28);
console.log(num3);


//pop method delete FIRST element from an array
var num4 = [23, 56, 12, 84, 17];
num4.shift();
console.log(num4);
